				      <h2><strong><span style="color: #333399;">¿Qué es la fisioterapia?</span></strong></h2>
<p>La Fisioterapia es la Ciencia de la salud que utiliza medios físicos a través de numerosas técnicas, que trata múltiples dolencias, agudas o crónicas:</p>
<ul>
 	<li>Prevenir y controlar el dolor, impedimentos</li>
 	<li>Discapacidades y limitaciones físicas.</li>
 	<li>Mejorar y mantener la independencia funcional y rendimiento de nuestra actividad.</li>
 	<li>Fomentar ese buen estado físico que repercute en nuestro bienestar y salud.</li>
</ul>

<strong style="font-size: 1.5em;">&nbsp;</strong>
<h2><strong><span style="color: #333399;">¿Qué hace el fisioterapeuta?</span></strong></h2>
<p>Es el profesional sanitario que realiza el tratamiento adecuado al diagnóstico y proporciona múltiples soluciones para sus problemas de salud.</p>

<p>Hay que saber que un buen fisioterapeuta empatiza con los pacientes desarrollando una sensibilidad especial de acercamiento a cada persona y esa sintonía, que suele ser la más valorada por los clientes, es parte intrínseca de la efectividad de nuestro trabajo, es decir, <strong>recuperar la calidad de vida y mantenerla</strong>.</p>

<p>La fisioterapia trata pacientes de todo tipo de edades, desde bebés hasta personas de la tercera edad. Previene y puede controlar los efectos de cualquier tipo de lesión física, patologías congénitas, neurológicas, reumatológicas, cardiopulmonares, traumatismos por accidentes y las consecuencias que el estrés y la tensión nerviosa de nuestro estilo de vida provocan en nuestro sistema motor y orgánico.</p>

<p>En Clínica Aguirre trabajamos "con usted" añadiendo normas saludables de conducta física, a medio y largo plazo.</p>

<p>Un tratamiento completo para tratar a las personas en su globalidad bio-psico-social. Recuperar la calidad de vida y continuarla es primordial. Esta labor la compartimos juntos.</p>

<img src="http://www.clinicaaguirre.com/imagenes-tratamientos/1_fisioterapia.jpg" alt="tratamiento de fisioterapia" width="100%" />

<h2><span style="color: #333399;"><strong>Tratamientos</strong></span></h2>

<h4><span style="color: #333399;"><strong>FISIOTERAPIA DEPORTIVA</strong></span></h4>
<p>Para todas las lesiones provocadas durante la práctica deportiva que deben ser tratadas en la mayor brevedad de tiempo. Mediante nuestras técnicas y métodos, siempre al día de los nuevos avances (Vendaje neuromuscular, electroestimulación, acupuntura, osteopatía, etc..), nuestros pacientes obtienen una reinserción a la actividad física de una forma rápida y segura.</p>

<p>Tratamiento de esguinces, distensiones, roturas fibrilares, tratamientos posquirúrgicos, etc., obtienen los mejores resultados con la fisioterapia.</p>
<h4><strong><span style="color: #333399;">FISIOTERAPIA RESPIRATORIA Y CARDIOVASCULAR</span>
</strong></h4>
<p>Disfunciones respiratorias como el EPOC, Asma, Bronquiectasias, etc., como patologías cardiovasculares; Postcirugía, requieren una reeducación del sistema cardiorrespiratorio para la recuperación de la capacidad pulmonar y adaptación al esfuerzo. El fisioterapeuta es el profesional encargado de esta misión, mediante ejercicios respiratorios, físicos y técnicas manuales ayudará a la adaptación a la vida diaria y prevención.</p>
<h4><span style="color: #333399;"><strong>FISIOTERAPIA TRAUMATOLÓGICA</strong></span></h4>
En Clínica Aguirre garantizamos la correcta recuperación de su problema.</p>

<p>Tras una fractura ósea, intervención quirúrgica, politraumatismo, etc. y su posterior inmovilización, viene el periodo de rehabilitación en el cual el fisioterapeuta se encarga de recuperar la movilidad articular, la fuerza muscular y la adaptación a la solicitación de las palancas del resto del cuerpo. Utilizando técnicas manuales, electroterapia, vendaje neuromuscular, pilates, etc.</p>
<h4><span style="color: #333399;"><strong>FISIOTERAPIA GERIÁTRICA</strong></span></h4>
<p>Las patologías del aparato musculoesquelético y sistema neurológico que afectan a nuestros ancianos como Artrosis, artritis, fracturas de cadera, hemiplejias, etc. En Clínica Aguirre llevamos varios años tratando estos problemas y solidarizados con ellos (Asociación de Altzheimer,..) ya que necesitan nuestros cuidados y la mejoría de sus patologías es realmente notable.</p>
					  
