				      <h2><span style="color: #333399;"><strong><strong>¿Qué es?</strong></strong></span></h2>
<p>El <strong>drenaje linfático</strong> manual es un tipo de masaje que consiste en maniobras y movimientos suaves,lentos y repetitivos que favorecen el funcionamiento del sistema linfático.</p>

<p>El SISTEMA LINFÁTICO es una&nbsp; red de vasos por los que circula la linfa ,(=liquido denso constituido por&nbsp;proteínas&nbsp; sales minerales, grasas, agua…), la cual se forma en los tejidos y circula en sentido ascendente hasta entrar en el sistema circulatorio a nivel del cuello.</p>

<p>Nuestro sistema linfático participa en la regulación de este equilibrio, ademas de ayudar al proceso de eliminación metabólica, intervenir en la respuesta inmunológica y ser fuente de purificación y limpieza. Prueba los masajes drenantes en nuestra&nbsp;<a href="http://www.clinicaaguirre.com/">clínica de fisioterapia en Madrid</a>.</p>
<h2><span style="color: #333399;"><strong><strong>¿Cuándo se Aplica?</strong></strong></span></h2>

- Inflamaciones o edemas: linfedemas, lipedemas, sinusitis, hematomas, cicatrices,&nbsp;después&nbsp;de operaciones, edemas por el embarazo.
- Dolores de los miembros musculares.
- Lesiones deportivas (esguinces…) y en dolores crónicos tipo artrósicos.
- Estrés, ansiedad.
- Migrañas, dolores de cabeza.
- Varices venosas.
- Estreñimientos.
- Dolores menstruales.
- Después de cancer tratados (por extirpación de ganglios).

