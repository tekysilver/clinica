<h2><span style="color: #333399;"><strong><strong>Efectos del Drenaje Linfático Manual</strong></strong></span></h2>

<p>Disminuir el edema: provoca una salida de liquido que no debe estar en los espacios intercelulares, para que por el sistema linfático conducirlo al torrente sanguíneo y eliminarlo.</p>

<p>En el sistema nervioso (antiestrés) :&nbsp;actúa&nbsp;directamente sobre el sistema neurovegetativo ya que al realizarse maniobras lentas,&nbsp;monótonas&nbsp;y rítmicas aumenta la&nbsp;función&nbsp;parasimpática&nbsp;de manera involuntaria.</p>

<p>En la musculatura: relaja la musculatura y regula el peristaltismo intestinal.</p>

<p>En el sistema inmunitario: favorece las defensas del organismo aumentando la producción de&nbsp;células&nbsp;y anticuerpos para la defensa de éste.</p>

<p>En el embarazo se producen una serie de cambios corporales en todas las mujeres que en muchos casos pueden provocar molestias y dolores, siendo casi la mitad de estos, de espalda, pudiendo existir desde los primeros meses hasta el final de la gestación.</p>
