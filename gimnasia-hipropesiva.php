				      <h2><span style="color: #333399;"><strong><strong>¿Qué es?</strong></strong></span></h2>
<p>La <strong>gimnasia</strong> hipopresiva es una disciplina dirigida a trabajar la zona abdominal, un método capaz de reducir la cintura y reforzar el suelo pélvico. <strong>Son ejercicios que te ayudan a tener una faja abdominal y un suelo pélvico competente</strong></p>

<p>Esta gimnasia, además, corrige la postura, coloca la columna sin dar presión abdominal, trabaja mucho los brazos y reduce la tensión del abdomen bajo.</p>

<p>Aunque están indicados en el postparto por sus beneficios en las estructuras más afectadas por el embarazo y el parto, esta gimnasia no solo está indicada para este colectivo. Se recomienda tanto a hombres como mujeres, deportistas o no deportistas. Al realizar clases de hipopresivos previenes de manera eficaz todo tipo de hernias, obtendremos mejoras en la postura, aumento de la fuerza de la musculatura de la espalda, y en el caso de la mujer evita el descenso de órganos pelvianos y la incontinencia urinaria.</p>

<p>Debido a la complejidad de estos ejercicios, lo más recomendable es acudir a un centro donde enseñan a realizarlos personalmente. Una vez aprendidos, también pueden practicarse en casa.</p>

<p>Tenemos una amplia experiencia en el trabajo con hipopresivos, tanto en deportistas como en recuperación postparto, ayudando a la recuperación del tono muscular del abdomen y el suelo pélvico. </p>

<h2><span style="color: #333399;"><strong><strong>¿Cuándo se Aplica?</strong></strong></span></h2>
<ul>
    <li>Lumbalgias crónicas.</li>
    <li>Inestabilidad lumbar.</li>
    <li>Diástasis de los rectos abdominales.</li>
    <li>Recuperar el tono del suelo pélvico.</li>
    <li>Reducción o freno a la incontinencia urinaria.</li>
    <li>Recuperar la postura (que ha sido modificada por el peso del bebe durante el embarazo).</li>
</ul>


