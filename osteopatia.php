				       <h2><span style="color: #333399; font-size: 27px;"><strong>¿Qué es la osteopatía?</strong></span></h2>
<p>La osteopatía es una terapia manual que&nbsp;se fundamenta en el trabajo conjunto de todos los sistemas del cuerpo que funcionan de manera interconectada y&nbsp;que están relacionados, desde la globalidad del individuo,&nbsp;y por consiguiente la disfunción de uno de ellos va a alterar el funcionamiento de los otros y viceversa.</p>
<h2><span style="color: #333399;">Osteopatía</span></h2>
<p>El <strong>tratamiento de osteopatía en Madrid</strong> estará indicado en los siguientes casos:</p>
<ul>
 	<li>Dolores de espalda</li>
 	<li>Dolores cervicales</li>
 	<li>Dolores de cabeza</li>
 	<li>Mareos</li>
 	<li>Lesiones deportivas</li>
 	<li>Trastornos digestivos</li>
 	<li>Lesiones agudas y crónicas</li>
</ul>

<p>Por lo tanto consistirá en realizar un trabajo de&nbsp;holístico. Según sus principios, las formas de abordaje, técnicas para la manipulación tanto del sistema músculo esquelético , tejido conectivo y disfunciones de órganos etc... ayuda y mejora el aporte sanguíneo a estas zonas y a recuperar y devolver el grado de movilidad normal a la articulación, músculo, órgano o membrana.</p>

<img src="http://www.clinicaaguirre.com/imagenes-tratamientos/2_osteopatia.jpg" alt="tratamiento de osteopatia" width="100%" />

<h5><span style="color: #333399; font-size: 27px;"><strong>Tipos</strong></span></h5>

<p><span style="color: #333399;"><strong>Osteopatía craneal:</strong></span> Se centra en los problemas craneales y su influencia en el Sistema nervioso central.</p>

<p><span style="color: #333399;"><strong>Osteopatía articular:</strong></span> Trabajo del restablecimiento del aparato músculo-esquelético y de la postura, centrándose en la columna vertebral y articulaciones.</p>

<p><span style="color: #333399;"><strong>Osteopatía visceral:</strong></span> Dirigida al tratamiento de órganos y vísceras, recuperando la movilidad y función de estos mediante técnicas específicas para aumentar el aporte sanguíneo.</p>

<p><b>El tratamiento osteopático</b> consiste en tratar esas disfunciones y busca recuperar el equilibrio corporal. En Clínica Aguirre te ofrecemos uno de los mejores <b>tratamientos osteopáticos de todo Madrid.</b></p>

<p><b>Osteopatía en Madrid</b> está indicado para una amplia variedad de trastornos y alteraciones:</p>
<ul>
 	<li>Esguinces, contracturas, tendinitis, dolores de espalda, cervicales, etc.</li>
 	<li>Digestivas: colon irritable, gases, hernia de hiato…</li>
 	<li>Sistema respiratorio: asma, tos, bronquitis</li>
 	<li>Cefaleas y migrañas</li>
 	<li>Estrés, ansiedad, trastornos del sueño, vértigo o cansancio generalizado</li>
</ul>

<p><span style="text-decoration: underline;">La osteopatía se basa en cinco principios fundamentales:</span></p>
<ol>
 	<li>La unidad del cuerpo. Todos los sistemas son independientes y están relacionados entre sí.</li>
 	<li>La estructura gobierna la función y viceversa</li>
 	<li>La auto curación. El cuerpo humano posee mecanismos para curarse solo, siempre y cuando todas sus estructuras se encuentren en equilibrio</li>
 	<li>La ley de la arteria es absoluta. El objetivo del tratamiento osteopático es mantener la circulación sanguínea sin obstáculos.</li>
 	<li>La vida es movimiento. Cada célula de nuestro cuerpo tiene movimiento, donde no hay movimiento, la célula está parada y el tejido muere. Para que los órganos puedan cumplir su función deben tener el movimiento adecuado.</li>
</ol>
<p>El <b>diagnóstico y tratamiento,</b> para que sean seguros, deben estar realizados por un <b>osteópata</b>. Un profesional que tiene una formación de 6 años, y está reconocido por el Registro de Osteópatas de España (ROE). Al igual que al hacer un <a href="http://www.clinicaaguirre.com/drenaje-linfatico">drenaje linfático en Madrid</a>, solo puede ser realizado por profesionales en la materia.</p>

<p>En Clínica Aguirre somos especialistas en osteopatía y fisioterapia, además de ofrecer tratamientos de <a href="http://www.clinicaaguirre.com/kinesiologia">kinesiología en Madrid</a>. ¡Llámanos y te informaremos sin compromiso!</p>
				      
